#include<iostream>
#include<bits/stdc++.h>
using namespace std;
int main() {
    long long int num1;
    long long int num2;
    cin>>num1>>num2;
    cout<<num1<<" + "<<num2<<" = "<<num1+num2<<endl;
    cout<<endl;
    cout<<num1<<" - "<<num2<<" = "<<num1-num2<<endl;
    cout<<endl;
    cout<<num1<<" * "<<num2<<" = "<<num1*num2<<endl;
    cout<<endl;
    cout<<num1<<" / "<<num2<<" = "<<num1/num2<<endl;
    cout<<endl;
    cout<<num1<<" % "<<num2<<" = "<<num1%num2<<endl;
}